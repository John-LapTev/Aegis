namespace Aegis.Core.Models;

/// <summary>
/// Запланированная «проверка после перезагрузки»: после рискованной правки (реестр/система) ждём подтверждения
/// пользователя, что всё работает; если не подтвердит — откатываем ИМЕННО сделанные правки по их бэкапам
/// (экспорт веток реестра, включение задач и т.п.) — тем же механизмом, что кнопка «Вернуть» и раздел «Бэкапы».
/// </summary>
public sealed record PendingRollback
{
    /// <summary>Идентификаторы точечных бэкапов правок пакета, которые откатываем при отказе/таймауте.</summary>
    public required IReadOnlyList<string> BackupIds { get; init; }

    /// <summary>Понятное описание, что именно меняли (показываем в окне подтверждения).</summary>
    public required string Description { get; init; }
}
