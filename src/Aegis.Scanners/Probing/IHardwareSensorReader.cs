namespace Aegis.Scanners.Probing;

/// <summary>
/// Чтение датчиков железа напрямую (температуры ядер, обороты вентиляторов, частоты) — за абстракцией,
/// чтобы сканеры/пробники не зависели от конкретной библиотеки и тестировались моками. Реализация
/// Windows-специфична и работает только там; на других ОС/при сбое отдаёт <see cref="HardwareReadings.Empty"/>.
/// </summary>
public interface IHardwareSensorReader
{
    /// <summary>Снять текущие показания датчиков. Никогда не бросает — при недоступности отдаёт пустые.</summary>
    HardwareReadings Read();
}
