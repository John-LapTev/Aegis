namespace Aegis.Scanners.Stress;

/// <summary>
/// Вид проверки под нагрузкой. <see cref="CpuSafe"/> — короткий безопасный прогрев (для всех),
/// <see cref="CpuDeep"/> — дольше (для тех, кто хочет проверить серьёзнее). Оба — с авто-стопом
/// по температуре и по таймеру-предохранителю: «жарить до сгорания» программа не умеет.
/// </summary>
public enum StressTestKind
{
    /// <summary>Короткий безопасный прогрев процессора (по умолчанию).</summary>
    CpuSafe,

    /// <summary>Углублённая проверка процессора — дольше, но с теми же предохранителями.</summary>
    CpuDeep,
}
